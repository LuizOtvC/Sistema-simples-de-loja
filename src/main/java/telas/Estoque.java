    public void listar_itens (){
                
        RoupasDAO dao = new RoupasDAO();   
        List <RoupasBean> lista = dao.ler();
       DefaultTableModel modelo = (DefaultTableModel) tabelaRoupas.getModel();
    modelo.setRowCount(0); // limpa a tabela antes de carregar

    for (RoupasBean r : lista) {
        modelo.addRow(new Object[]{
            r.getId(),
            r.getNome(),
            r.getPreco(),
            r.getQuantidade()
        });
    }
    }
    
